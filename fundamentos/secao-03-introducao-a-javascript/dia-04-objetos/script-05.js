const frutas = {};

for (let index = 0; index < basket.length; index += 1) {
    let currentFruit = basket[index];

    if (!frutas[currentFruit]) {
        frutas[currentFruit] = 1;
    } else {
        frutas[currentFruit] += 1;
    }
};

const resultado = Object.entries(frutas);

let novasFrutas = [];

for (let index = 0; index < resultado.length; index += 1) {
    if (resultado[index][1] > 1) {
        novasFrutas.push(`${resultado[index][1]} ${resultado[index][0]}s`);
    } else {
        novasFrutas.push(`${resultado[index][1]} ${resultado[index][0]}`);
    }
}

